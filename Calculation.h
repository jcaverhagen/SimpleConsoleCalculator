class Calculation
{
    public :
    int plusCalc(int firstNumber, int secondNumber) {
        return firstNumber + secondNumber;
    }

    int minusCalc(int firstNumber, int secondNumber) {
        return firstNumber - secondNumber;
    }

    int divideCalc(int firstNumber, int secondNumber) {
        return firstNumber / secondNumber;
    }

    int multiplyCalc(int firstNumber, int secondNumber) {
        return firstNumber * secondNumber;
    }
};
